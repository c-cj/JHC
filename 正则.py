import re
#匹配IP，可以匹配任意的 0到255的 1位数或3位数的IP
#如0.0.0.0 - 255.255.255.255
#000.000.000.000 - 255.255.255.255
ip = re.search(r'((?<![\.\d])(((25[0-5]|2[0-4]\d|[01]?\d\d|\d)\.){3})(25[0-5]|2[0-4]\d|[01]?\d\d|\d)(?![\.\d]))','aaa192.000.023.004aaa10.2.1.1\n1.1.1.1a\n10.2.2.2')
ip2 = re.findall ('((?<![\.\d])(((25[0-5]|2[0-4]\d|[01]?\d\d|\d)\.){3})(25[0-5]|2[0-4]\d|[01]?\d\d|\d)(?![\.\d]))','aaa192.000.023.004aaa10.2.1.1\n1.1.1.1a\n10.2.2.2')
for ip3 in  re.finditer('((?<![\.\d])(((25[0-5]|2[0-4]\d|[01]?\d\d|\d)\.){3})(25[0-5]|2[0-4]\d|[01]?\d\d|\d)(?![\.\d]))','aaa192.000.023.004aaa10.2.1.1\n1.1.1.1a\n10.2.2.2'):
    print(ip3.group())
reip = ip.group()

print("ip",reip)
print(ip2)

